import turtle

def draw_name():
    window=turtle.Screen()

    s=turtle.Turtle()
    s.backward(10)
    s.right(90)
    s.forward(10)
    s.left(90)
    s.forward(10)
    s.right(90)
    s.forward(10)
    s.right(90)
    s.forward(10)

    window.exitonclick()

draw_name()
    
    
